/* 
 *  Lab 4 Part A: Exploring nested for loops
 */

public class Lab4A
{
  public static void main(String[] args)
  {
     for (int cols = 0; cols < 3; cols++)
     {
       for (int rows = 0; rows < 4; rows++)
       {
         System.out.println("Row: " + rows + " Column: " + cols);
       }
      
     }
  }
}

