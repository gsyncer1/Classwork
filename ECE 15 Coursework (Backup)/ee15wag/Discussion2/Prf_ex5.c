#include <stdio.h>

/* Demonstrate the use of special characters like " and % 
using printf() */

int main()
{
   /* What if you want to print a " to the screen? */
   /* You can print " and % if you use the correct escape characters. */
   printf("My professor said: \n \"You should understand 100%% of this.\"\n");
   return 0;
}
