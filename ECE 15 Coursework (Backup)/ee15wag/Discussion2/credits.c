#include <stdio.h>

#define TOTAL_CREDITS 180 // Defines a constant

int main()
{
   int remaining_credits, completed_credits;
   
   // Prompt for credits
   printf("Please enter the number of credits you have completed: ");
   scanf("%d",&completed_credits); //&completed_credits stores number in that variable

   // calculate remaining credits
   remaining_credits = TOTAL_CREDITS - completed_credits;

   // Print out information to screen.
   printf("You have already completed %d credits.\n",completed_credits);
   printf("You still need to complete %d credits to graduate.\n",remaining_credits);

   return 0;
}
