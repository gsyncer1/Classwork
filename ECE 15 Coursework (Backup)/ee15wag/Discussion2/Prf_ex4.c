#include <stdio.h>

/* Creating a variable and printing the variable.
Also demonstrate precision. */

int main()
{
   double pi = 3.141592; // not int, but real number
   printf("The value of pi is %lf...\n",pi); // print floating point number
   printf("It is about %.2lf\n",pi);         // adjust the size
   return 0;
}
